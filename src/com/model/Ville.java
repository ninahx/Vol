package com.model;


public class Ville {
    private Long id;
    private String nomVille;
    // Getters et Setters
    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public String getNomVille() {
        return nomVille;
    }
    public void setNomVille(String nomVille) {
        this.nomVille = nomVille;
    }
}
